
public class telefoneCelular {
		private String marca;
	    private boolean funcionabilidade;
	    private String cor;
	    private String localFabricacao;
	    private int anoLancamento;

		public String getMarca() {
		    return marca;
		}
		
		public void setMarca(String marca) {
		    this.marca = marca;
		}
		
		public boolean getFuncionabilidade() {
		    return funcionabilidade;
		}
		
		public void setFuncionabilidade(boolean funcionabilidade) {
		    this.funcionabilidade = funcionabilidade;
		}
		
		public String getCor() {
		    return cor;
		}
		
		public void setCor(String cor) {
		    this.cor = cor;
		}
		
		public String getLocalFabricacao() {
		    return localFabricacao;
		}
		
		public void setLocalFabricacao(String localFabricacao) {
		    this.localFabricacao = localFabricacao;
		}
		
		public int getAnoLancamento() {
		    return anoLancamento;
		}
		
		public void setAnoLancamento(int anoLancamento) {
		    this.anoLancamento = anoLancamento;
		}
}